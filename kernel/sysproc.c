uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  backtrace();
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_sigalarm(void)
{ 
  struct proc *p = myproc();
  uint64 fnaddr;
  argaddr(1, &fnaddr);
  if (fnaddr >= p->sz)
    return -1;
  int ticks;
  argint(0, &ticks);
  p->nticks = ticks;
  p->countticks = 0;
  p->fnhandler = (void *) fnaddr;
  return 0;
}

uint64
sys_sigreturn(void)
{
 struct proc *p = myproc();
 if (p->sysreturn || p->nticks == 0 || p->countticks < p->nticks -1)
  return -1;
 p->trapframe[0] = p->trapframe[1];
 p->sysreturn = 1;
 //set counttick to zero
 p->countticks = 0;
 return 0;
}
